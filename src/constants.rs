#[derive(Clone, Copy, Debug, Eq, Hash, PartialEq, PartialOrd, Primitive)]
pub enum Type {
  Normal = 0,
  Fighting = 1,
  Flying = 2,
  Poison = 3,
  Ground = 4,
  Rock = 5,
  // Bird = 6,
  Bug = 7,
  Ghost = 8,
  Steel = 9,
  CurseType = 19,
  Fire = 20,
  Water = 21,
  Grass = 22,
  Electric = 23,
  Psychic = 24,
  Ice = 25,
  Dragon = 26,
  Dark = 27,
}
impl Type {
  #[inline] pub fn from_index(index: u8) -> Option<Type> {
    Type::from_u8(index)
  }
  #[inline] pub fn is_gen1(self) -> bool {
    self != Type::Steel && self != Type::CurseType && self != Type::Dark
  }
  #[inline] pub fn is_special_type(self) -> bool {
    self >= Type::Fire
  }
  #[inline] pub fn get_gen2_type_boost_badge(self) -> Option<u16> {
    match self {
      Type::Flying   => Some(0b0000_0000_0000_0001),
      Type::Bug      => Some(0b0000_0000_0000_0010),
      Type::Normal   => Some(0b0000_0000_0000_0100),
      Type::Ghost    => Some(0b0000_0000_0000_1000),
      Type::Steel    => Some(0b0000_0000_0001_0000),
      Type::Fighting => Some(0b0000_0000_0010_0000),
      Type::Ice      => Some(0b0000_0000_0100_0000),
      Type::Dragon   => Some(0b0000_0000_1000_0000),
      Type::Rock     => Some(0b0000_0001_0000_0000),
      Type::Water    => Some(0b0000_0010_0000_0000),
      Type::Electric => Some(0b0000_0100_0000_0000),
      Type::Grass    => Some(0b0000_1000_0000_0000),
      Type::Poison   => Some(0b0001_0000_0000_0000),
      Type::Psychic  => Some(0b0010_0000_0000_0000),
      Type::Fire     => Some(0b0100_0000_0000_0000),
      Type::Ground   => Some(0b1000_0000_0000_0000),
      _ => None,
    }
  }
}

