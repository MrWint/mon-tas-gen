#[derive(Clone, Copy, Debug, Eq, Hash, PartialEq)]
pub enum Status {
  Ok,
  Sleeping(u8),
  Poisoned,
  Burnt,
  Frozen,
  Paralyzed,
}
impl Status {
  pub fn from_u8(status: u8) -> Status {
    match status {
      0b0000_0000 => Status::Ok,
      i if i < 8 => Status::Sleeping(i),
      0b0000_1000 => Status::Poisoned,
      0b0001_0000 => Status::Burnt,
      0b0010_0000 => Status::Frozen,
      0b0100_0000 => Status::Paralyzed,
      _ => panic!("invlalid status {:x}", status),
    }
  }
}